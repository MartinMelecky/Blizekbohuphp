        <h3 class="knihy">Genesis</h3>
        <p class="kap">
          1. Tak byla dokončena nebesa i země se všemi svými zástupy. <br>
          2. Sedmého dne dokončil Bůh své dílo, které konal; sedmého dne přestal konat veškeré své dílo. <br>
          3. A Bůh požehnal a posvětil sedmý den, neboť v něm přestal konat veškeré své stvořitelské dílo. <br>
          4. Toto je rodopis nebe a země, jak byly stvořeny. V den, kdy Hospodin Bůh učinil zemi a nebe, <br>
          5. nebylo na zemi ještě žádné polní křovisko ani nevzcházela žádná polní bylina, neboť Hospodin Bůh nezavlažoval zemi deštěm, a nebylo člověka, který by zemi obdělával. <br>
          6. Jen záplava vystupovala ze země a napájela celý zemský povrch. <br>
          7. I vytvořil Hospodin Bůh člověka, prach ze země, a vdechl mu v chřípí dech života. Tak se stal člověk živým tvorem. <br>
          8. A Hospodin Bůh vysadil zahradu v Edenu na východě a postavil tam člověka, kterého vytvořil. <br>
          9. Hospodin Bůh dal vyrůst ze země všemu stromoví žádoucímu na pohled, s plody dobrými k jídlu, uprostřed zahrady pak stromu života a stromu poznání dobrého a zlého. <br>
          10. Z Edenu vychází řeka, aby napájela zahradu. Odtud dál se rozděluje ve čtyři hlavní toky. <br>
          11. Jméno prvního je Píšon; ten obtéká celou zemi Chavílu, v níž je zlato, <br>
          12. a zlato té země je skvělé; je tam také vonná pryskyřice a kámen karneol. <br>
          13. Jméno druhé řeky je Gíchón; ta obtéká celou zemi Kúš. <br>
          14. Jméno třetí řeky je Chidekel; ta teče východně od Asýrie. Čtvrtá řeka je Eufrat. <br>
          15. Hospodin Bůh postavil člověka do zahrady v Edenu, aby ji obdělával a střežil. <br>
          16. A Hospodin Bůh člověku přikázal: „Z každého stromu zahrady smíš jíst. <br>
          17. Ze stromu poznání dobrého a zlého však nejez. V den, kdy bys z něho pojedl, propadneš smrti.“ <br>
          18. I řekl Hospodin Bůh: „Není dobré, aby člověk byl sám. Učiním mu pomoc jemu rovnou.“ <br>
          19. Když vytvořil Hospodin Bůh ze země všechnu polní zvěř a všechno nebeské ptactvo, přivedl je k člověku, aby viděl, jak je nazve. Každý živý tvor se měl jmenovat podle toho, jak jej nazve. <br>
          20. Člověk tedy pojmenoval všechna zvířata a nebeské ptactvo i všechnu polní zvěř. Ale pro člověka se nenašla pomoc jemu rovná. <br>
          21. I uvedl Hospodin Bůh na člověka mrákotu, až usnul. Vzal jedno z jeho žeber a uzavřel to místo masem. <br>
          22. A Hospodin Bůh utvořil z žebra, které vzal z člověka, ženu a přivedl ji k němu. <br>
          23. Člověk zvolal: „Toto je kost z mých kostí a tělo z mého těla! Ať muženou se nazývá, vždyť z muže vzata jest.“ <br>
          24. Proto opustí muž svého otce i matku a přilne ke své ženě a stanou se jedním tělem. <br>
          25. Oba dva byli nazí, člověk i jeho žena, ale nestyděli se.  <br>
          
