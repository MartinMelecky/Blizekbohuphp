        <h3 class="knihy">Genesis 1</h3>
        <p class="kap">
          1.  Na počátku stvořil Bůh nebe a zemi. <br>
          2.  Země byla pustá a prázdná a nad propastnou tůní byla tma. Ale nad vodami vznášel se duch Boží. <br>
          3.  I řekl Bůh: „Buď světlo!“ A bylo světlo. <br>
          4.  Viděl, že světlo je dobré, a oddělil světlo od tmy. <br>
          5.  Světlo nazval Bůh dnem a tmu nazval nocí. Byl večer a bylo jitro, den první. <br>
          6.  I řekl Bůh: „Buď klenba uprostřed vod a odděluj vody od vod!“ <br>
          7.  Učinil klenbu a oddělil vody pod klenbou od vod nad klenbou. A stalo se tak. <br>
          8.  Klenbu nazval Bůh nebem. Byl večer a bylo jitro, den druhý. <br>
          9.  I řekl Bůh: „Nahromaďte se vody pod nebem na jedno místo a ukaž se souš!“ A stalo se tak. <br>
          10.  Souš nazval Bůh zemí a nahromaděné vody nazval moři. Viděl, že to je dobré. <br>
          11.  Bůh také řekl: „Zazelenej se země zelení: bylinami, které se rozmnožují semeny, a ovocným stromovím rozmanitého druhu, které na zemi ponese plody se semeny!“ A stalo se tak. <br>
          12.  Země vydala zeleň: rozmanité druhy bylin, které se rozmnožují semeny, a rozmanité druhy stromoví, které nese plody se semeny. Bůh viděl, že to je dobré. <br>
          13.  Byl večer a bylo jitro, den třetí. <br>
          14.  I řekl Bůh: „Buďte světla na nebeské klenbě, aby oddělovala den od noci! Budou na znamení časů, dnů a let. <br>
          15.  Ta světla ať jsou na nebeské klenbě, aby svítila nad zemí.“ A stalo se tak. <br>
          16.  Učinil tedy Bůh dvě veliká světla: větší světlo, aby vládlo ve dne, a menší světlo, aby vládlo v noci; učinil i hvězdy. <br>
          17.  Bůh je umístil na nebeskou klenbu, aby svítila nad zemí, <br>
          18.  aby vládla ve dne a v noci a oddělovala světlo od tmy. Viděl, že to je dobré. <br>
          19.  Byl večer a bylo jitro, den čtvrtý. <br>
          20.  I řekl Bůh: „Hemžete se vody živočišnou havětí a létavci létejte nad zemí pod nebeskou klenbou!“ <br>
          21.  I stvořil Bůh veliké netvory a rozmanité druhy všelijakých hbitých živočichů, jimiž se zahemžily vody, stvořil i rozmanité druhy všelijakých okřídlených létavců. Viděl, že to je dobré. <br>
          22.  A Bůh jim požehnal: „Ploďte a množte se a naplňte vody v mořích. Létavci nechť se rozmnoží na zemi.“ <br>
          23.  Byl večer a bylo jitro, den pátý. <br>
          24.  I řekl Bůh: „Vydej země rozmanité druhy živočichů, dobytek, plazy a rozmanité druhy zemské zvěře!“ A stalo se tak. <br>
          25.  Bůh učinil rozmanité druhy zemské zvěře i rozmanité druhy dobytka a rozmanité druhy všelijakých zeměplazů. Viděl, že to je dobré. <br>
          26.  I řekl Bůh: „Učiňme člověka, aby byl naším obrazem podle naší podoby. Ať lidé panují nad mořskými rybami a nad nebeským ptactvem, nad zvířaty a nad celou zemí i nad každým plazem plazícím se po zemi.“ <br>
          27.  Bůh stvořil člověka, aby byl jeho obrazem, stvořil ho, aby byl obrazem Božím, jako muže a ženu je stvořil. <br>
          28.  A Bůh jim požehnal a řekl jim: „Ploďte a množte se a naplňte zemi. Podmaňte ji a panujte nad mořskými rybami, nad nebeským ptactvem, nade vším živým, co se na zemi hýbe.“ <br>
          29.  Bůh také řekl: „Hle, dal jsem vám na celé zemi každou bylinu nesoucí semena i každý strom, na němž rostou plody se semeny. To budete mít za pokrm. <br>
          30.  Veškeré zemské zvěři i všemu nebeskému ptactvu a všemu, co se plazí po zemi, v čem je živá duše, dal jsem za pokrm veškerou zelenou bylinu.“ A stalo se tak. <br>
          31.  Bůh viděl, že všechno, co učinil, je velmi dobré. Byl večer a bylo jitro, den šestý. <br>
          
