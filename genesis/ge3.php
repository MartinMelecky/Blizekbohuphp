        <h3 class="knihy">Genesis</h3>
        <p class="kap">
          1. Nejzchytralejší ze vší polní zvěře, kterou Hospodin Bůh učinil, byl had. Řekl ženě: „Jakže, Bůh vám zakázal jíst ze všech stromů v zahradě?“ <br>
          2. Žena hadovi odvětila: „Plody ze stromů v zahradě jíst smíme. <br>
          3. Jen o plodech ze stromu, který je uprostřed zahrady, Bůh řekl: „Nejezte z něho, ani se ho nedotkněte, abyste nezemřeli.“ <br>
          4. Had ženu ujišťoval: „Nikoli, nepropadnete smrti. <br>
          5. Bůh však ví, že v den, kdy z něho pojíte, otevřou se vám oči a budete jako Bůh znát dobré i zlé.“ <br>
          6. Žena viděla, že je to strom s plody dobrými k jídlu, lákavý pro oči, strom slibující vševědoucnost. Vzala tedy z jeho plodů a jedla, dala také svému muži, který byl s ní, a on též jedl. <br>
          7. Oběma se otevřely oči: poznali, že jsou nazí. Spletli tedy fíkové listy a přepásali se jimi. <br>
          8. Tu uslyšeli hlas Hospodina Boha procházejícího se po zahradě za denního vánku. I ukryli se člověk a jeho žena před Hospodinem Bohem uprostřed stromoví v zahradě. <br>
          9. Hospodin Bůh zavolal na člověka: „Kde jsi?“ <br>
          10. On odpověděl: „Uslyšel jsem v zahradě tvůj hlas a bál jsem se. A protože jsem nahý, ukryl jsem se.“ <br>
          11. Bůh mu řekl: „Kdo ti pověděl, že jsi nahý? Nejedl jsi z toho stromu, z něhož jsem ti zakázal jíst?“ <br>
          12. Člověk odpověděl: „Žena, kterou jsi mi dal, aby při mně stála, ta mi dala z toho stromu a já jsem jedl.“ <br>
          13. Proto řekl Hospodin Bůh ženě: „Cos to učinila?“ Žena odpověděla: „Had mě podvedl a já jsem jedla.“ <br>
          14. I řekl Hospodin Bůh hadovi: „Protožes to učinil, budeš proklet, odvržen ode všech zvířat a ode vší polní zvěře. Polezeš po břiše, po všechny dny svého života žrát budeš prach. <br>
          15. Mezi tebe a ženu položím nepřátelství, i mezi símě tvé a símě její. Ono ti rozdrtí hlavu a ty jemu rozdrtíš patu.“ <br>
          16. Ženě řekl: „Velice rozmnožím tvé trápení i bolesti těhotenství, syny budeš rodit v utrpení, budeš dychtit po svém muži, ale on nad tebou bude vládnout.“ <br>
          17. Adamovi řekl: „Uposlechl jsi hlasu své ženy a jedl jsi ze stromu, z něhož jsem ti zakázal jíst. Kvůli tobě nechť je země prokleta; po celý svůj život z ní budeš jíst v trápení. <br>
          18. Vydá ti jenom trní a hloží a budeš jíst polní byliny. <br>
          19. V potu své tváře budeš jíst chléb, dokud se nenavrátíš do země, z níž jsi byl vzat. Prach jsi a v prach se navrátíš.“ <br>
          20. Člověk svou ženu pojmenoval Eva (to je Živa) , protože se stala matkou všech živých. <br>
          21. Hospodin Bůh udělal Adamovi a jeho ženě kožené suknice a přioděl je. <br>
          22. I řekl Hospodin Bůh: „Teď je člověk jako jeden z nás, zná dobré i zlé. Nepřipustím, aby vztáhl ruku po stromu života, jedl a byl živ navěky.“ <br>
          23. Proto jej Hospodin Bůh vyhnal ze zahrady v Edenu, aby obdělával zemi, z níž byl vzat. <br>
          24. Tak člověka zapudil. Východně od zahrady v Edenu usadil cheruby s míhajícím se plamenným mečem, aby střežili cestu ke stromu života. <br>
          
