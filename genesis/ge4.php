        <h3 class="knihy">Genesis</h3>
        <p class="kap">
          1. I poznal člověk svou ženu Evu a ta otěhotněla a porodila Kaina. Tu řekla: „Získala jsem muže, a tím Hospodina.“ <br>
          2. Dále porodila jeho bratra Ábela. Ábel se stal pastýřem ovcí, ale Kain se stal zemědělcem. <br>
          3. Po jisté době přinesl Kain Hospodinu obětní dar z plodin země. <br>
          4. Také Ábel přinesl oběť ze svých prvorozených ovcí a z jejich tuku. I shlédl Hospodin na Ábela a na jeho obětní dar, <br>
          5. na Kaina však a na jeho obětní dar neshlédl. Proto Kain vzplanul velikým hněvem a zesinal v tváři. <br>
          6. I řekl Hospodin Kainovi: „Proč jsi tak vzplanul? A proč máš tak sinalou tvář? <br>
          7. Což nepřijmu i tebe, budeš-li konat dobro? Nebudeš-li konat dobro, hřích se uvelebí ve dveřích a bude po tobě dychtit; ty však máš nad ním vládnout.“ <br>
          8. I promluvil Kain ke svému bratru Ábelovi... Když byli na poli, povstal Kain proti svému bratru Ábelovi a zabil jej. <br>
          9. Hospodin řekl Kainovi: „Kde je tvůj bratr Ábel?“ Odvětil: „Nevím. Cožpak jsem strážcem svého bratra?“ <br>
          10. Hospodin pravil: „Cos to učinil! Slyš, prolitá krev tvého bratra křičí ke mně ze země. <br>
          11. Nyní budeš proklet a odvržen od země, která rozevřela svá ústa, aby z tvé ruky přijala krev tvého bratra. <br>
          12. Budeš-li obdělávat půdu, už ti nedá svou sílu. Budeš na zemi psancem a štvancem.“ <br>
          13. Kain Hospodinu odvětil: „Můj zločin je větší, než je možno odčinit. <br>
          14. Hle, vypudil jsi mě dnes ze země. Budu se muset skrývat před tvou tváří. Stal jsem se na zemi psancem a štvancem. Každý, kdo mě najde, bude mě moci zabít.“ <br>
          15. Ale Hospodin řekl: „Nikoli, kdo by Kaina zabil, bude postižen sedmeronásobnou pomstou.“ A Hospodin poznamenal Kaina znamením, aby jej nikdo, kdo ho najde, nezabil. <br>
          16. Kain odešel od tváře Hospodinovy a usadil se v zemi Nódu, východně od Edenu. <br>
          17. I poznal Kain svou ženu, ta otěhotněla a porodila Enocha. Tu se dal do stavby města a nazval to město Enoch, podle jména svého syna. <br>
          18. Enochovi se narodil Írad, Írad zplodil Mechújáela, Mechíjáel zplodil Metúšáela, Metúšáel zplodil Lámecha. <br>
          19. Lámech pojal dvě ženy; jedna se jmenovala Áda a druhá se jmenovala Sila. <br>
          20. Áda porodila Jábala, který se stal praotcem těch, kdo přebývají ve stanu a u stáda. <br>
          21. Jeho bratr se jmenoval Júbal; ten se stal praotcem všech hrajících na citeru a flétnu. <br>
          22. Také Sila porodila, a to Túbal-kaina, mistra všech řemeslníků obrábějících měď a železo. Sestrou Túbal-kainovou byla Naama. <br>
          23. Tu řekl Lámech svým ženám: „Ádo a Silo, poslyšte můj hlas, ženy Lámechovy, naslouchejte mé řeči: Zabil jsem muže za své zranění, pacholíka za svou jizvu. <br>
          24. Bude-li sedmeronásobně pomstěn Kain, tedy Lámech sedmdesátkrát a sedmkrát.“ <br>
          25. I poznal opět Adam svou ženu a ta porodila syna a dala mu jméno Šét (to je [Do klína] vložený) . Řekla: „Bůh mi vložil do klína jiného potomka místo Ábela, kterého zabil Kain.“ <br>
          26. Šétovi se narodil syn; dal mu jméno Enóš. Tehdy se začalo vzývat jméno Hospodinovo. <br>
          
