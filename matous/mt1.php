        <h3 class="knihy">Matouš 1</h3>
        <p class="kap">
          1. Listina rodu Ježíše Krista, syna Davidova, syna Abrahamova. <br>
          2. Abraham měl syna Izáka, Izák Jákoba, Jákob Judu a jeho bratry, <br>
          3. Juda Farese a Záru z Támary, Fares měl syna Chesróma, Chesróm Arama. <br>
          4. Aram měl syna Amínadaba, Amínadab Naasona, Naason Salmóna, <br>
          5. Salmón měl syna Bóaza z Rachaby, Bóaz Obéda z Rút, Obéd Isaje <br>
          6. a Isaj Davida krále. David měl syna Šalomouna z ženy Uriášovy, <br>
          7. Šalomoun Roboáma, Roboám Abiu, Abia Asafa, <br>
          8. Asaf Jóšafata, Jóšafat Jórama, Jóram Uziáše. <br>
          9. Uziáš měl syna Jótama, Jótam Achaza, Achaz Ezechiáše, <br>
          10. Ezechiáš Manase, Manase měl syna Amose, Amos Joziáše, <br>
          11. Joziáš Jechoniáše a jeho bratry za babylónského zajetí. <br>
          12. Po babylónském zajetí Jechoniáš měl syna Salatiela, Salatiel Zorobabela, <br>
          13. Zorobabel Abiuda, Abiud Eliakima, Eliakim Azóra, <br>
          14. Azór Sádoka, Sádok Achima. Achim měl syna Eliuda, <br>
          15. Eliud Eleazara, Eleazar Mattana, Mattan Jákoba, <br>
          16. Jákob pak měl syna Josefa, muže Marie, z níž se narodil Ježíš, řečený Kristus. <br>
          17. Všech pokolení od Abrahama do Davida bylo tedy čtrnáct, od Davida po babylónské zajetí čtrnáct a od babylónského zajetí až po Krista čtrnáct. <br>
          18. Narození Ježíše Krista se událo takto: Jeho matka Maria byla zasnoubena Josefovi, ale dříve než se sešli, shledalo se, že počala z Ducha svatého. <br>
          19. Její muž Josef byl spravedlivý a nechtěl ji vystavit hanbě; proto se rozhodl propustit ji potají. <br>
          20. Ale když pojal ten úmysl, hle, anděl Páně se mu zjevil ve snu a řekl: „Josefe, synu Davidův, neboj se přijmout Marii, svou manželku; neboť co v ní bylo počato, je z Ducha svatého. <br>
          21. Porodí syna a dáš mu jméno Ježíš; neboť on vysvobodí svůj lid z jeho hříchů.“ <br>
          22. To všechno se stalo, aby se splnilo, co řekl Hospodin ústy proroka: <br>
          23. "Hle, panna počne a porodí syna a dají mu jméno Immanuel," to jest přeloženo "Bůh s námi". <br>
          24. Když se Josef probudil ze spánku, učinil, jak mu přikázal anděl Hospodinův, a přijal svou manželku k sobě. <br>
          25. Ale nežili spolu, dokud neporodila syna; a dal mu jméno Ježíš.<br>
          
